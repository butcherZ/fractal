#include "libft.h"

int		ft_count_digits(long nb)
{
	int	counter;

	counter = 1;
	if (nb < 0)
	{
		nb = -nb;
	}
	while (nb >= 10)
	{
		nb = nb / 10;
		counter++;
	}
	return (counter);
}
